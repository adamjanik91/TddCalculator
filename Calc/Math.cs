namespace Calc
{
    public static class Math
    {
        public static long Add(long a, long b)
        {
            var result = a + b;
            return result;
        }

        public static long Subtract(long a, long b)
        {
            var result = a - b;
            return result;
        }

        public static long Divide(long a, long b)
        {
            var result = a / b;
            return result;
        }

        public static long Multiply(long a, long b)
        {
            var result = a * b;
            return result;
        }
    }
}
